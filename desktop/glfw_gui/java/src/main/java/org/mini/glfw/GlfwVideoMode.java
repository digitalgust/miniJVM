package org.mini.glfw;

public class GlfwVideoMode {
	public int width;
	public int height;
	public int redBits;
	public int greenBits;
	public int blueBits;
	@Override
	public String toString() {
		return "GlfwVideoMode [width=" + width + ", height=" + height
				+ ", redBits=" + redBits + ", greenBits=" + greenBits
				+ ", blueBits=" + blueBits + "]";
	}
}
